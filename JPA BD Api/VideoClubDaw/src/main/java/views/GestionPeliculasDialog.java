    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        int row = tablaPeliculas.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una película para editar.");
            return;
        }
        Pelicula seleccionada = peliculas.get(row);
        FormularioPeliculaDialog form = new FormularioPeliculaDialog(
                (java.awt.Frame) this.getParent(), true, seleccionada, peliculaController);
        form.addWindowListener(new java.awt.event.WindowAdapter() {
            @Override
            public void windowClosed(java.awt.event.WindowEvent e) {
                cargarTablaPeliculas();
            }
        });
        form.setVisible(true);
    }//GEN-LAST:event_btnEditarActionPerformed

