    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        int row = tablaPeliculas.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona una película para eliminar.");
            return;
        }
        Pelicula seleccionada = peliculas.get(row);
        int confirm = JOptionPane.showConfirmDialog(this, "¿Seguro que quieres eliminar la película \"" + seleccionada.getTitulo() + "\"?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            peliculaController.eliminarPelicula(seleccionada.getIdPelicula());
            cargarTablaPeliculas();
            JOptionPane.showMessageDialog(this, "Película eliminada.");
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

