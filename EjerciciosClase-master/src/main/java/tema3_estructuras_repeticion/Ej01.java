
package tema3_estructuras_repeticion;


public class Ej01 {

   
    public static void main(String[] args) {
        
        
         int x = 1;
       
        while (x<=100) {
            System.out.print(x);
            System.out.print(" - ");
            x = x + 1;
        }
        //Imprime número del 1 al 100 ya que cada iteración se le suma uno al número inicial
    }
    
}
