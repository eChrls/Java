
package tema3_estructuras_repeticion;


public class Ej08 {

   
    public static void main(String[] args) {
        
        
        int j=1;
		for (int i= -13; i<=-10; i++) {
			j++;
		}
		System.out.println(j);
                
                //Al empezar el bucle j ya vale 2 la iteracion termina ahi y se suma 1 a i
                //Por lo tanto tras 3 iteraciones j vale 5
        
        
        
    }
    
}
