    /**
     * Carga todas las películas en la tabla.
     */
    private void cargarTablaPeliculas() {
        peliculas = peliculaController.listarPeliculas();
        String[] columnas = {"ID", "Título", "Año", "Portada"};
        DefaultTableModel modelo = new DefaultTableModel(null, columnas) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        for (Pelicula p : peliculas) {
            Object[] fila = {
                p.getIdPelicula(),
                p.getTitulo(),
                p.getAnyoEstreno(),
                p.getPortada()
            };
            modelo.addRow(fila);
        }

        tablaPeliculas.setModel(modelo);
        tablaPeliculas.setRowHeight(30);
    }

